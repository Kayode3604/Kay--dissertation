def transform_climate(folder_name, output_name):
    folder_path = os.path.join(base_path, folder_name)

    nc_files = [
        os.path.join(folder_path, f)
        for f in os.listdir(folder_path)
        if f.endswith('.nc')
    ]

    all_data = []

    for file in nc_files:
        print(f"Processing {file}")

        ds = xr.open_dataset(file)
        df = ds.to_dataframe().reset_index()

        if 'crs' in df.columns:
            df = df.drop(columns=['crs'])

        climate_col = [c for c in df.columns if c not in ['time', 'lat', 'lon']][0]

        df['time'] = pd.to_datetime(df['time'])
        df['year'] = df['time'].dt.year

        yearly = df.groupby('year')[climate_col].mean().reset_index()
        yearly = yearly.rename(columns={climate_col: output_name})

        all_data.append(yearly)

    final_df = pd.concat(all_data).groupby('year').mean().reset_index()

    return final_df

