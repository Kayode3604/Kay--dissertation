import pandas as pd
import xarray as xr
import glob
import os
import re

def convert_daily_nc_to_yearly(folder_path, climate_column, method="mean"):
    files = sorted(glob.glob(os.path.join(folder_path, "**", "*.nc"), recursive=True))
    records = []

    print("Files found:", len(files))

    for file in files:
        filename = os.path.basename(file)

        try:
            ds = xr.open_dataset(file)

            var_name = list(ds.data_vars.keys())[0]
            data = ds[var_name]


            spatial_dims = [
                d for d in data.dims
                if d.lower() in ["lat", "latitude", "lon", "longitude"]
            ]

            if spatial_dims:
                daily_value = float(data.mean(dim=spatial_dims, skipna=True).values)
            else:
                daily_value = float(data.mean(skipna=True).values)


            match = re.search(r"(\d{4})", filename)
            if match:
                year = int(match.group(1))
                records.append([year, daily_value])

            ds.close()

        except Exception as e:
            print("Skipped:", filename, "|", e)

    daily_df = pd.DataFrame(records, columns=["year", "daily_value"])

    if method == "mean":
        yearly_df = daily_df.groupby("year")["daily_value"].mean().reset_index()
    elif method == "sum":
        yearly_df = daily_df.groupby("year")["daily_value"].sum().reset_index()

    yearly_df.columns = ["year", climate_column]
    return yearly_df

